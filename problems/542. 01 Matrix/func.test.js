const { func } = require("./func");

test("Example 1", () => {
  const mat = [
    [0, 0, 0],
    [0, 1, 0],
    [0, 0, 0],
  ];

  const expected = [
    [0, 0, 0],
    [0, 1, 0],
    [0, 0, 0],
  ];

  expect(func(mat)).toEqual(expected);
});
test("Example 2", () => {
  const mat = [
    [0, 0, 0],
    [0, 1, 0],
    [1, 1, 1],
  ];

  const expected = [
    [0, 0, 0],
    [0, 1, 0],
    [1, 2, 1],
  ];

  expect(func(mat)).toEqual(expected);
});
